import javax.swing.JOptionPane;


public class KnockKnock {
	public static void main(String[] args) {
		JOptionPane.showMessageDialog(null, "Knock knock!");
		JOptionPane.showMessageDialog(null, "Who's there?");
		JOptionPane.showMessageDialog(null, "Woo.");
		JOptionPane.showMessageDialog(null, "Woo who?");
		JOptionPane.showMessageDialog(null, "Calm down, I didn't get to the punchline yet.");		
	}
}
